const calculator = {
    add: function (a, b) {
        console.log(a + b);
    },
    minus: function (a, b) {
        console.log(a - b);
    },
    div: function (a, b) {
        console.log(a / b);
    },
    multi: function (a, b) {
        console.log(a * b);
    },
    powerof: function (a, b) {
        console.log(a ** b);
    },
};

calculator.add(5, 1);
calculator.minus(5, 1);
calculator.div(5, 1);
calculator.multi(5, 1);
calculator.powerof(5, 1);
